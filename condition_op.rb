weather = 'rainy'

case weather
when 'bright'
    puts "Bright it is, be cool!"
when 'dark'
    puts "Have your jacket!"
when 'rainy'
    puts "Jacket and umbrella needed!"
when 'stormy'
    puts "Whatever your wearing now, just stay home!"
when 'windy'
    puts "Jeans, shirt and the windbreaker will do!"
else
    puts "FGS, don't know what to wear!"
end   
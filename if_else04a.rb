fav_color = 'blue'

case fav_color
when 'red'
    puts "Red like fire!"
when 'orange'
    puts "Orange...like an orange!"
when 'yellow'
    puts "Yellow daffodils are pretty in the spring!"
when 'green'
    puts "Have you been to the forest?"
when 'pink'
    puts "Pretty in pink!"
when 'purple'
    puts "Purple plums are the tastiest."
when 'blue'
    puts "Blue like the sky!"    
else
    puts "Hmm, well I don't know what that color is!"
end   